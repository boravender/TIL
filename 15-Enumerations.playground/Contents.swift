import UIKit

/*
 
 ** Enumerations
 연관된 케이스들을 하나의 이름으로 묶은 자료형, 코드의 가독성과 안전성이 높아짐
 
 ** Raw Values(원시값)
 내부에 또다른 값을 저장할 수 있음
 자료형은 String, Character, Number Types으로 제한
 
 ** Associated Values(연관값)
 저장할 값의 형식을 개별 케이스마다 따로 선언, 하나의 케이스에 서로 다른 연관값 저장 가능
 
 ** Enumeration Case Pattern
 연관값을 가진 케이스를 매칭 시킬 때 사용
 => switch문, if문, guard문, for in문, while문
 
 */
